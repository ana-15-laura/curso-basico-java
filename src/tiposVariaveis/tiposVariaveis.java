package tiposVariaveis;

public class tiposVariaveis {

	public static void main(String[] args) {
		
		int numero = 5; //números inteiros
		float numeroFlutuante = 7.55F; //números decimais simples
		double numeroComQuebrado= 7.55; // números decimais duplo
		String nome = "Ana Laura"; // cadeia de caractere
		char caractere = 'M'; // um único caractere
		
		System.out.println(numero);
		System.out.println(numeroFlutuante);
		System.out.println(numeroComQuebrado);
		System.out.println(nome);
		System.out.println(caractere);
	}

}
